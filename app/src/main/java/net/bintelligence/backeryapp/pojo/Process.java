package net.bintelligence.backeryapp.pojo;

public class Process {
    private int processId;
    private String workingUnit;
    private String workingValue;
    private String processName;

    public int getProcessId() {
        return processId;
    }

    public void setProcessId(int processId) {
        this.processId = processId;
    }

    public String getWorkingUnit() {
        return workingUnit;
    }

    public void setWorkingUnit(String workingUnit) {
        this.workingUnit = workingUnit;
    }

    public String getWorkingValue() {
        return workingValue;
    }

    public void setWorkingValue(String workingValue) {
        this.workingValue = workingValue;
    }

    public String getProcessName() {
        return processName;
    }

    public void setProcessName(String processName) {
        this.processName = processName;
    }
}
